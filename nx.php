                <h1>Nintendo NX: novo console da Nintendo</h1>
            <br>
            <img src="nintendo.png" class="foto" alt="nx">
            <br><br>
                <p>Nintendo NX é o possível nome do próximo videogame da empresa japonesa. De acordo com rumores, o aparelho uniria as funções e design de um console tradicional com uma versão portátil. Essa união não deve ser algo completamente novo para a Nintendo, já que o controle do Wii U apresentava uma tela touchscreen que remetia a um tablet. O dispositivo deve utilizar um hardware mais poderoso para fugir das pesadas críticas à performance de Wii e Wii U. Não há data de lançamento confirmada e nem mais detalhes revelados, mas a previsão é que o NX chegue em 2017, acompanhado de um novo jogo da série Zelda.
	   <br><br>
                    A Nintendo começou a enviar os kits de desenvolvimento de seu novo console às produtoras, de acordo com o jornal The Wall Street Journal.
                    <br><br>
                    Como sempre acontece antes do anúncio de um novo console, os kits de desenvolvimento de software trazem também os primeiros detalhes de como será o hardware. De acordo com as fontes do jornal, o Nintendo NX irá incluir "uma unidade móvel que pode ser usada em conjunto com o console ou levada para a rua e ser usada separadamente".
                    <br>
                    Este detalhe alimenta os boatos de que a Nintendo pode substituir não apenas o Wii U em breve como também introduzir o sucessor do Nintendo 3DS. Recentemente, uma patente de um portátil com botões de ombro e tela tátil, não muito diferente do atual GamePad do Wii U, foi descoberta e apontada como sendo o possível controle do Nintendo NX.
                    <br><img src="controle.jpg" class="foto" alt="controle"><br>
                    Ainda segundo as fontes do jornal, a Nintendo estaria usando "chips líderes da indústria" no Nintendo NX, em resposta às críticas de que o Wii U é um console pouco poderoso.<br><br>
                    Apesar da data de lançamento estar longe, de acordo com Tatsumi Kimishima, presidente da Nintendo, isso pode ser positivo, já que o console deverá ser lançado com o preço bem alto e até lá temos bastante tempo pra economizar uma graninha.<br>
                    "Quando o Wii U foi lançado, o Yen era bastante forte. Eu estou trabalhando com a hipótese que essa situação não se repetirá. Vender [o console] com prejuízo pode não permitir que o modelo de negócios funcione, então não estamos considerando isso enquanto desenvolvemos o NX", revelou o cara durante uma sessão de perguntas em que apresentou os lucros da Nintendo.<br><br>
